
public class ex01 {

	/*
	 * Contador com "for" para incremento.
	 * 
	 * Estrutura repetitiva "para". Usar quando se sabe previamente a quantidade de
	 * repetições. Ótimo para fazer contagens(progressiva ou regressiva).
	 */

	public static void main(String[] args) {

		for (int i = 0; i < 5; i++) {
			System.out.println("Valor de i: " + i);
		}
	}
}
